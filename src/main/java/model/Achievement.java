package model;

public class Achievement {

    int Achievement_Id;
    String Achievement_Name;
    int Achievement_Points;
    int Achievement_Cost;

    public int getAchievement_Id() {
        return Achievement_Id;
    }

    public void setAchievement_Id(int achievement_Id) {
        Achievement_Id = achievement_Id;
    }

    public String getAchievement_Name() {
        return Achievement_Name;
    }

    public void setAchievement_Name(String achievement_Name) {
        Achievement_Name = achievement_Name;
    }

    public int getAchievement_Points() {
        return Achievement_Points;
    }

    public void setAchievement_Points(int achievement_Points) {
        Achievement_Points = achievement_Points;
    }

    public int getAchievement_Cost() {
        return Achievement_Cost;
    }

    public void setAchievement_Cost(int achievement_Cost) {
        Achievement_Cost = achievement_Cost;
    }
}
